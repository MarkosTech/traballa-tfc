    <div id="manual-instalacion">
        <h3>5.1 Manual de instalación</h3>
        <p>El manual de instalación proporciona instrucciones detalladas para configurar el sistema Traballa en un entorno de producción:</p>
        <ol>
            <li><strong>Requisitos previos:</strong> Servidor web Apache, PHP 8.0+, MySQL 8.0+, Composer</li>
            <li><strong>Descarga del código:</strong> Instrucciones para clonar el repositorio desde GitHub</li>
            <li><strong>Configuración de la base de datos:</strong> Creación de la base de datos y ejecución de scripts SQL</li>
            <li><strong>Configuración del entorno:</strong> Ajuste de parámetros en archivos de configuración</li>
            <li><strong>Instalación de dependencias:</strong> Uso de Composer para instalar paquetes requeridos</li>
            <li><strong>Configuración del servidor web:</strong> Ajustes de Apache y PHP para optimizar el rendimiento</li>
            <li><strong>Verificación de la instalación:</strong> Pruebas para confirmar el correcto funcionamiento</li>
        </ol>
        <p>El manual incluye también procedimientos para actualizar el sistema y solucionar problemas comunes de instalación.</p>
    </div>
    
    <div id="manual-usuario">
        <h3>5.2 Manual de usuario</h3>
        <p>El manual de usuario proporciona una guía completa para utilizar todas las funcionalidades de Traballa:</p>
        <ul>
            <li><strong>Primeros pasos:</strong> Registro, inicio de sesión y configuración inicial del perfil</li>
            <li><strong>Gestión de tiempo:</strong> Cómo registrar tiempo, iniciar temporizadores y categorizar el trabajo</li>
            <li><strong>Trabajo con proyectos:</strong> Creación y gestión de proyectos, invitación de colaboradores</li>
            <li><strong>Uso del tablero Kanban:</strong> Organización de tareas, cambio de estados, filtros</li>
            <li><strong>Calendario:</strong> Visualización y programación de actividades</li>
            <li><strong>Generación de informes:</strong> Configuración, visualización y exportación de reportes</li>
            <li><strong>Administración:</strong> Gestión de usuarios, permisos y configuraciones del sistema</li>
        </ul>
        <p>El manual incluye capturas de pantalla y ejemplos prácticos para facilitar el aprendizaje de los usuarios.</p>
        
