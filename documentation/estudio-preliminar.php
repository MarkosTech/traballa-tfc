    <h2>1. Estudio preliminar</h2>
    <div class="section-description">
        <p>En esta sección se presenta el estudio preliminar del proyecto, incluyendo los antecedentes, objetivos y un análisis de viabilidad.</p>
    </div>
    
    <div id="antecedentes">
        <h3>1.1 Antecedentes</h3>
        <p>La gestión eficiente del tiempo y los recursos es un desafío crítico para profesionales y empresas en el entorno laboral actual. Las herramientas existentes a menudo carecen de integración entre la gestión de tiempo, proyectos y reportes, creando ineficiencias y dificultando el seguimiento preciso del progreso del trabajo.</p>
        <p>Traballa surge como respuesta a esta necesidad, ofreciendo una solución integral que combina registro de horas, gestión de proyectos mediante metodologías ágiles (Kanban) y generación de informes detallados en una única plataforma accesible desde cualquier dispositivo.</p>
        <p>El proyecto nace de la necesidad personal de contar con una herramienta que fuera a la vez simple, potente y de código abierto, aprovechando las ventajas del software libre para crear una solución que pueda ser adoptada y mejorada por la comunidad.</p>
    </div>
    
    <div id="objetivos">
        <h3>1.2 Objetivos</h3>
        <p>Los objetivos principales del proyecto Traballa son:</p>
        <ul>
            <li><strong>Objetivo principal:</strong> Desarrollar una plataforma web completa, open source y orientada al uso personal para la gestión integrada de tiempos de trabajo y proyectos</li>
            <li><strong>Registro de tiempo eficiente:</strong> Implementar un sistema de fichar entrada y salida simple e intuitivo que permita a los usuarios registrar su actividad diaria con mínimos clics</li>
            <li><strong>Gestión ágil de proyectos:</strong> Crear un sistema de gestión de proyectos basado en la metodología Kanban para visualizar y organizar tareas de forma visual</li>
            <li><strong>Analytics y reportes:</strong> Diseñar un módulo de generación de informes para análisis de productividad, seguimiento de tiempo y ayuda en facturación</li>
            <li><strong>Experiencia de usuario moderna:</strong> Desarrollar una interfaz de usuario moderna, responsiva y accesible desde diferentes dispositivos</li>
            <li><strong>Colaboración multiusuario:</strong> Implementar un sistema de organizaciones y usuarios con diferentes niveles de permisos para facilitar la colaboración en equipos</li>
            <li><strong>Código abierto:</strong> Crear una solución de código abierto que pueda ser utilizada, modificada y mejorada por la comunidad</li>
            <li><strong>Demostración académica:</strong> Servir como proyecto de fin de ciclo que demuestre competencias en desarrollo web full-stack</li>
        </ul>
    </div>
    
    <div id="descripcion-proyecto">
        <h3>1.3 Descripción del proyecto</h3>
        <p>Traballa es una aplicación web integral para la gestión de tiempo y proyectos que combina un sistema de registro de horas de trabajo con herramientas de gestión de proyectos basadas en metodología Kanban. Su objetivo principal es proporcionar a profesionales independientes, equipos pequeños y medianos una plataforma unificada para controlar su productividad, organizar tareas y generar informes detallados sobre su actividad laboral.</p>
        
        <h4>1.3.1 Xustificación del proyecto</h4>
        <p>La idea del proyecto surgió de la necesidad personal de contar con una herramienta que integrara el registro de tiempo de trabajo con la gestión de proyectos de forma simple y eficiente. Tras analizar las soluciones existentes en el mercado, se identificó que muchas de ellas son excesivamente complejas, costosas o no ofrecen la flexibilidad necesaria para diferentes tipos de usuarios.</p>
        <p>El objetivo principal es resolver el problema de la fragmentación de herramientas que enfrentan los profesionales, proporcionando una solución que unifique el registro de tiempo, la gestión de tareas y la generación de informes en una sola plataforma accesible y fácil de usar.</p>
        
        <h4>1.3.2 Funcionalidades del proyecto</h4>
        <p>Traballa ofrecerá las siguientes funcionalidades principales:</p>
        <ul>
            <li><strong>Registro de tiempo:</strong> Sistema de fichar entrada y salida con seguimiento de pausas y descansos</li>
            <li><strong>Gestión de proyectos:</strong> Organización de trabajo por proyectos con descripción y estado</li>
            <li><strong>Tablero Kanban:</strong> Visualización y gestión de tareas mediante metodología ágil</li>
            <li><strong>Calendario integrado:</strong> Planificación y visualización de eventos y tareas programadas</li>
            <li><strong>Informes y analytics:</strong> Generación de reportes detallados sobre productividad y tiempo invertido</li>
            <li><strong>Gestión de usuarios:</strong> Sistema de organizaciones y proyectos compartidos</li>
            <li><strong>Exportación de datos:</strong> Funcionalidades para exportar informes en diferentes formatos</li>
        </ul>
        
        <h4>1.3.3 Estudio de necesidades</h4>
        <p>En el mercado actual existen diversas aplicaciones que resuelven problemas similares, cada una con diferentes enfoques:</p>
        <ul>
            <li><strong>Toggl Track:</strong> Excelente para registro de tiempo, pero limitada en gestión de proyectos. Tiene una buena posición en el mercado pero es de pago para funcionalidades avanzadas.</li>
            <li><strong>Trello:</strong> Muy popular para gestión de proyectos con Kanban, pero carece de funcionalidades de registro de tiempo.</li>
            <li><strong>Asana:</strong> Completa pero compleja, orientada a grandes equipos y con curva de aprendizaje elevada.</li>
            <li><strong>Monday.com:</strong> Potente pero costosa, dirigida principalmente a empresas medianas y grandes.</li>
            <li><strong>Clockify:</strong> Buena para registro de tiempo con algunas funcionalidades de proyectos, pero interfaz menos intuitiva.</li>
        </ul>
        <p>Traballa se diferencia al ofrecer una solución integrada, simple y accesible que combina lo mejor de ambos mundos: registro de tiempo eficiente y gestión de proyectos ágil.</p>
        
        <h4>1.3.4 Persoas destinatarias</h4>
        <p>La aplicación está dirigida principalmente a:</p>
        <ul>
            <li><strong>Profesionales independientes y freelancers:</strong> Que necesitan controlar su tiempo de trabajo y organizar proyectos de clientes</li>
            <li><strong>Pequeños equipos de trabajo:</strong> Especialmente en el sector tecnológico, consultoría y servicios creativos</li>
            <li><strong>Startups y empresas pequeñas:</strong> Que requieren herramientas de gestión sin la complejidad de soluciones empresariales</li>
            <li><strong>Trabajadores remotos:</strong> Que necesitan herramientas para autogestionar su tiempo y productividad</li>
            <li><strong>Estudiantes y académicos:</strong> Para gestionar proyectos de investigación y tiempo de estudio</li>
        </ul>
        <p>El rango de edad objetivo está entre 25-45 años, con conocimientos básicos de tecnología y necesidad de organización profesional.</p>
        
        <h4>1.3.5 Promoción</h4>
        <p>Las técnicas de promoción seleccionadas incluyen:</p>
        <ul>
            <li><strong>Código abierto (Open Source):</strong> Publicación del código en GitHub para atraer desarrolladores y crear una comunidad</li>
            <li><strong>Redes sociales profesionales:</strong> LinkedIn, Twitter/X para llegar a profesionales y equipos</li>
            <li><strong>Plataformas de desarrolladores:</strong> Product Hunt, Hacker News, Reddit para visibilidad en comunidades tecnológicas</li>
            <li><strong>SEO y marketing de contenidos:</strong> Blog con artículos sobre productividad y gestión de tiempo</li>
            <li><strong>Participación en eventos:</strong> Meetups locales de tecnología y emprendimiento</li>
            <li><strong>Colaboraciones:</strong> Partnerships con consultoras y freelancers para casos de uso reales</li>
        </ul>
        
        <h4>1.3.6 Modelo de negocio</h4>
        <p>Traballa adopta un modelo híbrido que combina:</p>
        <ul>
            <li><strong>Open Source:</strong> El código fuente es público y libre, permitiendo contribuciones de la comunidad</li>
            <li><strong>Freemium:</strong> Versión gratuita con funcionalidades básicas para uso personal</li>
            <li><strong>SaaS (Software as a Service):</strong> Versión hosted de pago para equipos que prefieren no gestionar la infraestructura</li>
            <li><strong>Self-hosted:</strong> Opción de instalación propia para organizaciones que requieren control total de sus datos</li>
        </ul>
        <p>Esta aproximación permite maximizar la adopción inicial mientras se genera valor para usuarios que requieren funcionalidades avanzadas o soporte profesional.</p>
