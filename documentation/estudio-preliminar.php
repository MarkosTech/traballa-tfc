    <div id="metodologia-desarrollo">
        <h3>1.4 Metodología de desarrollo y herramientas</h3>
        <p>En el panorama actual del desarrollo de software, las herramientas de Inteligencia Artificial se han convertido en recursos fundamentales que complementan las habilidades del desarrollador. En este proyecto se ha aprovechado el uso de Large Language Models (LLMs) como una herramienta más del arsenal de desarrollo, similar al uso de IDEs, frameworks o librerías.</p>
        
        <h4>1.4.1 Uso de LLMs en el desarrollo</h4>
        <p>Los LLMs han sido utilizados en este proyecto para:</p>
        <ul>
            <li><strong>Generación de código base:</strong> Creación de estructuras iniciales y patrones comunes</li>
            <li><strong>Documentación:</strong> Generación de comentarios de código y documentación técnica</li>
            <li><strong>Resolución de problemas:</strong> Asistencia en debugging y optimización de código</li>
            <li><strong>Diseño de interfaces:</strong> Sugerencias para mejoras en UX/UI</li>
            <li><strong>Testing:</strong> Generación de casos de prueba y validaciones</li>
        </ul>
        <p>Es importante destacar que el uso de estas herramientas no reemplaza el conocimiento técnico del desarrollador, sino que actúa como un acelerador que permite enfocarse en la lógica de negocio y la arquitectura del sistema.</p>
        
        <h4>1.4.2 Enfoque Open Source</h4>
        <p>El proyecto está diseñado principalmente para uso personal y como contribución a la comunidad open source. Los beneficios de este enfoque incluyen:</p>
        <ul>
            <li><strong>Transparencia:</strong> El código fuente está disponible para revisión y auditoría</li>
            <li><strong>Colaboración:</strong> Posibilidad de recibir contribuciones de otros desarrolladores</li>
            <li><strong>Aprendizaje:</strong> Sirve como ejemplo de implementación para otros estudiantes y profesionales</li>
            <li><strong>Flexibilidad:</strong> Los usuarios pueden modificar y adaptar el software a sus necesidades específicas</li>
            <li><strong>Sostenibilidad:</strong> La comunidad puede mantener y evolucionar el proyecto a largo plazo</li>
        </ul>
        
        <h4>1.4.3 Justificación del uso de PHP Vanilla</h4>
        <p>La decisión de utilizar PHP sin frameworks (PHP Vanilla) para el desarrollo de Traballa responde principalmente a los objetivos académicos y pedagógicos del proyecto como Trabajo de Fin de Ciclo del Grado Superior en Desarrollo de Aplicaciones Web.</p>
        
        <h5>Coherencia con el programa formativo</h5>
        <p>El stack tecnológico seleccionado se alinea directamente con las competencias y tecnologías desarrolladas durante el ciclo formativo DAW:</p>
        <ul>
            <li><strong>PHP nativo:</strong> Permite demostrar un dominio profundo del lenguaje sin dependencias externas, evidenciando comprensión de los fundamentos del desarrollo web del lado servidor</li>
            <li><strong>HTML5, CSS3 y JavaScript:</strong> Tecnologías base del desarrollo frontend abordadas en los módulos de Desarrollo Web en Entorno Cliente y Diseño de Interfaces Web</li>
            <li><strong>MySQL:</strong> Sistema de gestión de bases de datos estudiado en el módulo de Bases de Datos</li>
            <li><strong>Arquitectura web tradicional:</strong> Aplicación directa de los conceptos de desarrollo web del lado servidor del módulo correspondiente</li>
        </ul>
        
        <h5>Ventajas educativas del enfoque vanilla</h5>
        <p>El desarrollo sin frameworks aporta beneficios específicos en el contexto académico:</p>
        <ul>
            <li><strong>Comprensión integral:</strong> Obliga a implementar desde cero funcionalidades que los frameworks abstraen, demostrando comprensión completa de los procesos</li>
            <li><strong>Control total:</strong> Permite tomar decisiones arquitectónicas conscientes sobre cada aspecto del sistema</li>
            <li><strong>Depuración efectiva:</strong> Facilita la identificación y resolución de problemas al conocer todo el código implementado</li>
            <li><strong>Portabilidad:</strong> El código resultante es más portable y no depende de versiones específicas de frameworks</li>
            <li><strong>Demostración de competencias:</strong> Evidencia capacidad para desarrollar aplicaciones complejas con herramientas básicas</li>
        </ul>
        
        <h5>Justificación técnica</h5>
        <p>Además de las razones académicas, el enfoque vanilla se justifica técnicamente para este proyecto:</p>
        <ul>
            <li><strong>Simplicidad del dominio:</strong> Las funcionalidades de Traballa no requieren las abstracciones complejas que proporcionan los frameworks</li>
            <li><strong>Rendimiento:</strong> Menor overhead y mayor control sobre el rendimiento de la aplicación</li>
            <li><strong>Mantenibilidad:</strong> Para un proyecto de este alcance, el código vanilla es más fácil de mantener y entender</li>
            <li><strong>Escalabilidad controlada:</strong> Permite implementar solo las funcionalidades necesarias sin bloat adicional</li>
        </ul>
        
        <p>Esta aproximación demuestra que es posible crear aplicaciones web completas y funcionales utilizando exclusivamente las tecnologías fundamentales, validando la solidez de la formación recibida en el ciclo DAW y la capacidad del desarrollador para aplicar estos conocimientos en proyectos reales.</p>
    </div>
    
    <div id="requirimentos">
        <h3>1.5 Requirimentos</h3>
        <p>Los medios materiales y tecnologías necesarias para el desarrollo del proyecto incluyen:</p>
        
        <h4>1.5.1 Infraestructura</h4>
        <ul>
            <li><strong>Servidor web:</strong> Apache/Nginx para servir la aplicación PHP</li>
