            <li><strong>Dominio web:</strong> Para la versión de demostración online</li>
            <li><strong>SSL/TLS:</strong> Certificados de seguridad para conexiones HTTPS</li>
            <li><strong>Almacenamiento:</strong> Espacio en disco para archivos del sistema y backups</li>
            <li><strong>CDN:</strong> Para optimización de recursos estáticos (opcional)</li>
        </ul>
        
        <h4>1.5.2 Backend</h4>
        <ul>
            <li><strong>PHP 8.1+:</strong> Lenguaje principal del backend</li>
            <li><strong>MySQL/MariaDB:</strong> Sistema de gestión de base de datos</li>
            <li><strong>Composer:</strong> Gestor de dependencias PHP</li>
            <li><strong>PHPUnit:</strong> Framework de testing para PHP</li>
            <li><strong>PHPMailer:</strong> Librería para envío de emails</li>
            <li><strong>Arquitectura por capas:</strong> Separación básica de responsabilidades sin MVC estricto</li>
        </ul>
        
        <h4>1.5.3 Frontend</h4>
        <ul>
            <li><strong>HTML5:</strong> Estructura semántica de las páginas</li>
            <li><strong>CSS3:</strong> Estilos y diseño responsivo</li>
            <li><strong>JavaScript ES6+:</strong> Interactividad y funcionalidades del cliente</li>
            <li><strong>Mermaid.js:</strong> Generación de diagramas y gráficos</li>
            <li><strong>Chart.js:</strong> Gráficos para analytics y reportes</li>
            <li><strong>Fetch API:</strong> Comunicación asíncrona con el servidor</li>
        </ul>
        
        <h4>1.5.4 Herramientas de desarrollo</h4>
        <ul>
            <li><strong>Git:</strong> Control de versiones</li>
            <li><strong>GitHub:</strong> Repositorio remoto y colaboración</li>
            <li><strong>VS Code:</strong> Entorno de desarrollo integrado</li>
            <li><strong>XAMPP/Docker:</strong> Entorno de desarrollo local</li>
