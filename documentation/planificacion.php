    <div id="fases">
        <h3>6.1 Fases del proyecto</h3>
        <p>El desarrollo de Traballa se ha dividido en las siguientes fases, ajustadas para un periodo de 3 meses con una dedicación de 16 horas semanales:</p>
        <ol>
            <li><strong>Estudio preliminar y análisis (Semanas 1-2):</strong> Definición del alcance, objetivos, viabilidad del proyecto y análisis de requisitos.</li>
            <li><strong>Diseño (Semanas 3-4):</strong> Arquitectura del sistema, modelo de datos, diseño de interfaces y prototipación.</li>
            <li><strong>Codificación y pruebas (Semanas 5-10):</strong>
                <ul>
                    <li>Semana 5: Desarrollo de la infraestructura base y módulo de usuarios</li>
                    <li>Semana 6: Implementación del módulo de registro de tiempo</li>
                    <li>Semana 7: Desarrollo de gestión de proyectos</li>
                    <li>Semana 8: Implementación del tablero Kanban</li>
                    <li>Semana 9: Desarrollo del calendario y sistema de eventos</li>
                    <li>Semana 10: Implementación del sistema de informes y analytics</li>
                </ul>
            </li>
            <li><strong>Testing y optimización (Semana 11):</strong> Pruebas integrales, corrección de errores y optimización de rendimiento.</li>
            <li><strong>Documentación y entrega (Semana 12):</strong> Elaboración de manuales de usuario, documentación técnica y preparación de la presentación final.</li>
        </ol>
        <p><strong>Carga de trabajo:</strong> 16 horas semanales durante 10 semanas = 176 horas totales de desarrollo</p>
    </div>
    
