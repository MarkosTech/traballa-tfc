    <h2>8. Conclusiones</h2>
    <p>El desarrollo del proyecto Traballa ha permitido aplicar de manera integrada las competencias adquiridas durante el ciclo formativo DAW, especialmente en áreas como:</p>
    <ul>
        <li>Desarrollo de aplicaciones web con arquitecturas modernas</li>
        <li>Diseño y gestión de bases de datos relacionales</li>
        <li>Implementación de interfaces de usuario responsivas y accesibles</li>
        <li>Integración de diferentes tecnologías y frameworks</li>
        <li>Gestión de proyectos de desarrollo de software</li>
    </ul>
    <p>Los principales logros del proyecto incluyen:</p>
    <ul>
        <li>Creación de un sistema completo que cumple con todos los requisitos establecidos</li>
        <li>Implementación de una interfaz de usuario intuitiva y eficiente</li>
        <li>Desarrollo de un sistema modular y extensible que permite futuras ampliaciones</li>
        <li>Aplicación de buenas prácticas de desarrollo y estándares de la industria</li>
    </ul>
    <p>Como propuestas de mejora para futuras versiones, se identifican:</p>
    <ul>
        <li>Implementación de una API REST completa para facilitar integraciones con otros sistemas</li>
        <li>Desarrollo de aplicaciones móviles nativas para mejorar la experiencia en dispositivos móviles</li>
        <li>Incorporación de funcionalidades avanzadas de analítica y business intelligence</li>
        <li>Mejora del sistema de notificaciones con opciones personalizables por usuario</li>
    </ul>
